main() {
  var x = 20;
  var y = 3;

  print(x + y);
  print(x - y);
  print(x * y);
  print(x / y);
  print(x ~/ y);
  print(x % y);
  print(-(x - y));

  print("x + y = ${x + y}");
  print("x - y = ${x - y}");
  print("x * y = ${x * y}");
  print("x / y = ${x / y}");
  print("x ~/ y = ${x ~/ y}");
  print("x % y = ${x % y}");
  print("-(x - y) = ${-(x - y)}");
}
