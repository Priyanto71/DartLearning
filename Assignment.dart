main() {
  double a = 10;
  print("a = $a"); //10.0
  a += 5;
  print("a = $a"); //15.0
  a -= 10;
  print("a = $a"); //5.0
  a *= 6;
  print("a = $a"); //30.0
  a /= 10;
  print("a = $a"); //3.0
  a %= 2;
  print("a = $a"); //1.0
  var b = 20;
  print("b = $b"); //20
  b ~/= 10;
  print("b = $b"); //2
}
